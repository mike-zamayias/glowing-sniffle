x, y = 10, 2

print(f'x + y = {x + y}')   #   addition
print(f'x - y = {x - y}')   #   substraction
print(f'x * y = {x * y}')   #   multiplication
print(f'x / y = {x / y}')   #   division
print(f'x % y = {x % y}')   #   modulus
print(f'x // y = {x // y}') #   floor divion
print(f'x ** y = {x ** y}') #   exponent
