x, y = 10, 2

print(f'x < y = {x < y}')   #   lesser than
print(f'x > y = {x > y}')   #   greater than
print(f'x == y = {x == y}') #   equal to
print(f'x != y = {x != y}') #   not equal to
print(f'x <= y = {x <= y}') #   lesser than or equal to
print(f'x >= y = {x >= y}') #   greater than or equal to
